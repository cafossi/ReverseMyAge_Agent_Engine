def build_table_with_controls(
    table_id: str,
    search_box_id: str,
    headers: List[str],
    rows: List[List[str]],
    export_filename: str
) -> str:
    """Build table with search and export controls using chrome theme."""
    headers_html = ""
    for idx, header in enumerate(headers):
        headers_html += f'<th onclick="sortTable(\'{table_id}\', {idx})">{header}</th>'
    
    rows_html = ""
    for row in rows:
        rows_html += "<tr>"
        for cell in row:
            rows_html += f"<td>{cell}</td>"
        rows_html += "</tr>"
    
    return f"""
    <div class="table-container">
        <div class="table-controls">
            <input type="text" 
                   id="{search_box_id}" 
                   class="search-box" 
                   placeholder="üîç Search table..." 
                   onkeyup="filterTable('{table_id}', '{search_box_id}')">
            <button class="export-btn" onclick="exportTableToCSV('{table_id}', '{export_filename}')">
                üì• Export to CSV
            </button>
        </div>
        <table id="{table_id}">
            <thead>
                <tr>{headers_html}</tr>
            </thead>
            <tbody>
                {rows_html}
            </tbody>
        </table>
    </div>
    """
