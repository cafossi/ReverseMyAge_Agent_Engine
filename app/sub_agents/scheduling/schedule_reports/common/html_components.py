def get_javascript() -> str:
    """JavaScript for interactive features."""
    return """
    <script>
        // Toggle main sections
        function toggleSection(sectionId) {
            const section = document.getElementById(sectionId);
            if (section) {
                section.classList.toggle('expanded');
            }
        }

        // Toggle site subsections
        function toggleSubsection(siteId) {
            const subsection = document.getElementById('subsection-' + siteId);
            if (subsection) {
                subsection.classList.toggle('expanded');
            }
        }

        // Expand section programmatically
        function expandSection(sectionId) {
            const section = document.getElementById(sectionId);
            if (section && !section.classList.contains('expanded')) {
                section.classList.add('expanded');
            }
        }

        // Scroll to element with offset
        function scrollToSection(elementId, offset = 80) {
            const element = document.getElementById(elementId);
            if (element) {
                const elementPosition = element.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - offset;

                window.scrollTo({
                    top: offsetPosition,
                    behavior: 'smooth'
                });
            }
        }

        // Navigate to section and expand
        function navigateToSection(sectionId) {
            expandSection(sectionId);
            setTimeout(() => {
                scrollToSection(sectionId);
            }, 100);
        }

        // Navigate to site detail
        function navigateToSite(siteId) {
            expandSection('section-site-details');
            const subsectionId = 'subsection-' + siteId;
            setTimeout(() => {
                expandSection(subsectionId);
                scrollToSection(subsectionId);
            }, 300);
        }

        // Sort table by column
        function sortTable(tableId, columnIndex) {
            const table = document.getElementById(tableId);
            if (!table) return;

            const tbody = table.getElementsByTagName('tbody')[0];
            const rows = Array.from(tbody.getElementsByTagName('tr'));

            const currentDirection = table.getAttribute('data-sort-direction') || 'asc';
            const newDirection = currentDirection === 'asc' ? 'desc' : 'asc';
            table.setAttribute('data-sort-direction', newDirection);

            rows.sort((a, b) => {
                const aCell = a.getElementsByTagName('td')[columnIndex];
                const bCell = b.getElementsByTagName('td')[columnIndex];

                if (!aCell || !bCell) return 0;

                let aValue = aCell.textContent.trim();
                let bValue = bCell.textContent.trim();

                const aNum = parseFloat(aValue.replace(/[^0-9.-]/g, ''));
                const bNum = parseFloat(bValue.replace(/[^0-9.-]/g, ''));

                if (!isNaN(aNum) && !isNaN(bNum)) {
                    return newDirection === 'asc' ? aNum - bNum : bNum - aNum;
                }

                return newDirection === 'asc' 
                    ? aValue.localeCompare(bValue) 
                    : bValue.localeCompare(aValue);
            });

            rows.forEach(row => tbody.appendChild(row));
        }

        // Filter table rows
        function filterTable(tableId, searchBoxId) {
            const input = document.getElementById(searchBoxId);
            const filter = input.value.toUpperCase();
            const table = document.getElementById(tableId);

            if (!table) return;

            const tbody = table.getElementsByTagName('tbody')[0];
            const rows = tbody.getElementsByTagName('tr');

            for (let i = 0; i < rows.length; i++) {
                const cells = rows[i].getElementsByTagName('td');
                let found = false;

                for (let j = 0; j < cells.length; j++) {
                    const cellText = cells[j].textContent || cells[j].innerText;
                    if (cellText.toUpperCase().indexOf(filter) > -1) {
                        found = true;
                        break;
                    }
                }

                rows[i].style.display = found ? '' : 'none';
            }
        }

        // Export table to CSV
        function exportTableToCSV(tableId, filename) {
            const table = document.getElementById(tableId);
            if (!table) return;

            let csv = [];
            const rows = table.querySelectorAll('tr');

            for (let row of rows) {
                let rowData = [];
                const cells = row.querySelectorAll('th, td');

                for (let cell of cells) {
                    let text = cell.textContent.trim();
                    text = text.replace(/"/g, '""');
                    if (text.includes(',')) {
                        text = '"' + text + '"';
                    }
                    rowData.push(text);
                }

                csv.push(rowData.join(','));
            }

            const csvContent = csv.join('\\n');
            const blob = new Blob([csvContent], { type: 'text/csv' });
            const url = window.URL.createObjectURL(blob);
            const a = document.createElement('a');
            a.setAttribute('href', url);
            a.setAttribute('download', filename);
            a.click();
        }

        document.addEventListener('DOMContentLoaded', function() {
            const paretoSection = document.getElementById('section-pareto-matrix');
            if (paretoSection) {
                paretoSection.classList.add('expanded');
            }
        });
    </script>
    """


