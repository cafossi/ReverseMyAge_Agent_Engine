def build_metadata_cards(cards: List[Dict[str, str]]) -> str:
    """Build metadata cards grid (Waymo-style meta panel)."""
    cards_html = ""
    for card in cards:
        # Get conditional class if specified
        card_class = card.get('class', '')
        class_attr = f' class="metadata-card {card_class}"' if card_class else ' class="metadata-card"'
        
        cards_html += f"""
        <div{class_attr}>
            <div class="metadata-label">{card['label']}</div>
            <div class="metadata-value">{card['value']}</div>
        </div>
        """
    
    return f"""
    <div class="metadata-grid">
        {cards_html}
    </div>
    """


